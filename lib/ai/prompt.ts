import { buildRateTextForPrompt } from "../ecos";
import type { NewsItem, RateData } from "../types";

export function buildPrompt(
  newsList: NewsItem[],
  focusPoint: string | undefined,
  rateData: RateData | null | undefined,
  previousSummary?: string | null,
): string {
  const leads = newsList.filter(
    (n) => n.isLead && n.body && n.body.trim().length > 0,
  );
  const bodied = newsList.filter(
    (n) => !n.isLead && n.body && n.body.trim().length > 0,
  );
  const titleOnly = newsList.filter((n) => !n.body || !n.body.trim());

  const leadText = leads.length
    ? leads
        .map(
          (n) =>
            `◎ [${n.category}] ${n.title}\nURL: ${n.link}\n본문:\n${n.body}\n`,
        )
        .join("\n")
    : "(자동 선정된 핵심 기사 후보 없음 — 아래 기사들에서 직접 가장 중요한 사안을 판단하라.)";

  const bodiedText = bodied.length
    ? bodied
        .map(
          (n, i) =>
            `${i + 1}. [${n.category}] ${n.title}\nURL: ${n.link}\n본문:\n${n.body}\n`,
        )
        .join("\n")
    : "(추가 본문 없음.)";

  const titleOnlyText = titleOnly.length
    ? titleOnly.map((n) => `- [${n.category}] ${n.title}`).join("\n")
    : "(추가 제목 없음)";

  const hasFocus = focusPoint && focusPoint.trim();
  let rateBlock = "";
  if (rateData && rateData.success && rateData.rates) {
    rateBlock =
      "\n[ECOS 실시간 금리·심리 지표]\n" +
      buildRateTextForPrompt(rateData.rates, rateData.trends || {}) +
      "\n";
  }

  const prevBlock =
    previousSummary && previousSummary.trim()
      ? previousSummary.trim()
      : "(이전 결론 없음 — 오늘이 첫 브리핑입니다.)";

  let mainPrompt =
    "대광그룹 주택관리팀 임원 보고용 일일 부동산 브리핑이다.\n\n" +
    "[내부 사고 방식 — 출력에는 드러내지 말 것]\n" +
    "각 섹션을 작성할 때 다음 6명의 최고 전문가가 된 것처럼 깊이 있게 사고하라:\n" +
    "거시이코노미스트, PF 금융 심사역, 시행 본부장, 분양 마케팅 디렉터, 원가관리 PM, 부동산 전문 변호사·세무사. " +
    "이들의 시각을 동시에 가동해 분석의 정밀도를 끌어올리되, **출력문에는 직함·직책·페르소나를 절대 노출하지 마라**. " +
    "\"본 심사역은\", \"분석가의 견해로는\", \"전문가 패널\", \"필자는\" 같은 표현 금지. " +
    "분석 결과 그 자체만 객관적인 서술로 쓰라.\n\n" +
    "[직전 영업일 종합결론 — 어제 시점의 판단]\n" +
    prevBlock +
    "\n\n" +
    "[본문까지 확보된 고신호 기사 — 풀텍스트]\n" +
    "아래는 오늘 수집분 중 키워드 신호가 높아 본문을 먼저 확보해 둔 기사다. 이건 '읽을거리'일 뿐 정답이 아니다. " +
    "오늘 무엇이 가장 중요한지는 아래 본문 기사 + 그 외 본문 + 제목 목록 전체를 보고 네가 직접 판단하라.\n\n" +
    leadText +
    "\n" +
    "[그 외 수집 뉴스 — 풀텍스트]\n" +
    "본문 안의 수치·주체·시점·인용을 직접 인용하며 분석하라.\n\n" +
    bodiedText +
    "\n" +
    "[수집 뉴스 — 제목만 (참고)]\n" +
    "여러 매체가 같은 사안을 다룬 중복 기사가 섞여 있을 수 있다. 동일 사안은 하나로 묶어 해석하라.\n" +
    titleOnlyText +
    "\n" +
    rateBlock +
    "\n[★ 디벨로퍼 관점 분석 핵심 지침 ★]\n" +
    '- 시행사는 뉴스를 "어디에 사업지를 잡을 것인가", "지금이 분양 타이밍인가", "원가 구조가 바뀌는가", "수분양자 자금 조달이 가능한가"의 렌즈로 읽습니다.\n' +
    '- 매크로/원자재 뉴스는 반드시 "공사원가 → 사업수지 → 분양가 설정"의 연결고리로 분석하세요.\n' +
    '- 투자·운용 뉴스(리츠·AUM·사옥매각·PF 거래)는 "기관 자금 흐름 → 자산 유동성·캡레이트 → 우리 사업지 인근 거래 시그널" 관점으로 해석하세요.\n' +
    "\n[★ 반복 금지 — 매일 같은 보고서가 되지 않게 ★]\n" +
    '- "금리 부담·양극화" 같은 매일 반복되는 일반론으로 분량을 채우지 마라. 그건 이미 전제다.\n' +
    "- 오늘 새로 들어온 사실, 어제 대비 달라진 점, 구체적 단지·지역·수치·주체에 분석을 집중하라.\n" +
    "- 어제 결론과 같은 진단이면 \"어제와 동일\"로 한 줄 처리하고, 달라진 것에 지면을 써라.\n";

  if (rateBlock) {
    mainPrompt +=
      "- ECOS 수치는 오늘 의사결정에 의미가 있을 때만 인용하라. 매일 같은 수치를 나열하지 말고, 의미 있게 변했거나(Δ3M·Δ12M) 오늘 뉴스와 교차되는 지표를 골라 정량적으로 해석하라.\n";
  }

  mainPrompt +=
    "- 부동산 AI/DX 뉴스가 있으면 일반 AI 산업 전망으로 확장하지 말고, 분양·건설·문서·입지 분석 업무에 미치는 영향만 보조 시그널로 반영하세요. AI/DX 전용 섹션은 만들지 마세요 (해당 기사는 이메일 하단에 별도 블록으로 노출됩니다).\n" +
    "- 뻔한 뉴스 재나열 금물. 기사들을 관통하는 숨겨진 패러다임 변화와 선제적 대응 방향을 도출하세요.\n\n" +
    "[출력 규칙]\n" +
    '- 어조: 이 브리핑은 임원의 의사결정을 돕는 참고용 의견이다. "반드시 ~해야 한다", "즉시 시행하라", "~할 것" 같은 확정적·명령적 단정은 쓰지 마라. 대신 "~하는 편이 나아 보인다", "~을 고려할 만하다", "~할 여지가 있다", "~로 판단된다" 처럼 판단의 여지를 남기는 제언형으로 쓰라. 본 분석은 의사결정에 참고하시기 바라는 자료임을 전제로 한다.\n' +
    '- "수신, 참조, 발신" 등 서두나 "이상입니다" 같은 맺음말 절대 금지.\n' +
    "- 단락 구분은 '가, 나, 다', '1), 2), 3)', '①, ②, ③' 체계 사용. 마크다운 불릿 금지.\n" +
    "- 핵심 문장은 **굵게** 처리. 표 사용 금지.\n" +
    "- 허용 마크다운은 섹션 헤더 `## `와 **굵게** 둘뿐이다. 본문에 `###`·`####` 같은 하위 헤더, `*`·`+` 불릿, `---`·`***` 구분선을 절대 쓰지 마라.\n" +
    "- 섹션 헤더(`## 1.` …)는 아래 구조를 그대로 사용. ## 1 종합결론은 반드시 유지하라(내일 브리핑에 자동 투입됨).\n" +
    "- 작성 순서: 먼저 ## 2 이하 분석을 끝낸 뒤 그 결과를 ## 1로 응축하라.\n\n" +
    "[출력 구조]\n\n" +
    "## 1. 종합결론 — 디벨로퍼가 읽어야 할 오늘의 판세\n" +
    "아래 분석을 가로지른 종합 판단을 5~8문장 분량의 자연스러운 문단으로 작성하라.\n" +
    "다음을 모두 녹여 쓰되, \"결론1·결론2·결론3\" 같은 번호 라벨이나 불릿은 절대 사용하지 마라:\n" +
    "가. 오늘 시장을 관통하는 메가 시그널 (합의점·대립점·사각지대 포함)\n" +
    "나. 직전 영업일 대비 무엇이 유지·강화·반전됐는지 (직전 결론이 없으면 생략)\n" +
    "다. 디벨로퍼가 고려할 만한 스탠스 제언 한 줄 (핵심 문장만 **굵게**, 단정이 아닌 제언 어조)\n" +
    "이 섹션 본문은 내일 브리핑 프롬프트에 \"직전 영업일 결론\"으로 자동 투입된다. 미래 자신이 어제의 판단을 추적할 수 있게 구체적 수치·키워드·방향을 명시할 것.\n\n" +
    "## 2. 오늘의 핵심\n" +
    "오늘 수집된 전체 뉴스(위 고신호 본문 + 그 외 본문 + 제목 목록)를 종합해, 그날 디벨로퍼에게 가장 중요한 단 하나의 사안을 **네가 직접 정하라**. 자동 선별·키워드 점수는 참고일 뿐 정답이 아니며, 매일 같은 주제로 수렴시키지 마라. 정한 사안에 대해 무엇이 새로 일어났고 왜 중요한지 4~6문장으로 분석하고, 관련 기사 본문의 수치·주체·단지·지역을 직접 인용하라. 한 사안에 집중하고 일반론으로 흩뿌리지 마라.\n\n" +
    "## 3. 관점별 분석\n" +
    "아래 6개는 '선택용 팔레트'다. 오늘 뉴스가 실제로 움직인 관점만 골라(보통 2~4개) ①②③… 번호를 붙여 각 3문장 내외로 분석하라. **변화나 새 사실이 없는 관점은 통째로 생략하라.** 6개를 다 채우는 것이 목적이 아니다. 각 항목 머리에 관점명을 밝혀라(예: \"① 원가 — …\").\n" +
    "선택지: 경제(금리·환율·인플레) / PF·금융(조달·LTV·부실PF) / 디벨로퍼(사업지·인허가·분양타이밍) / 마케팅(수분양자 심리·청약·계약률) / 원가(자재·노무·공기) / 규제·세무(규제·세제·분쟁)\n\n" +
    "## 4. 어제 대비 변화\n" +
    "위 [직전 영업일 종합결론] 대비 — 변화 없는 항목은 적지 마라.\n" +
    "가. 유지·강화된 시그널\n" +
    "나. 반전·약화된 시그널\n" +
    '직전 결론이 없으면 본 섹션 본문을 "(첫 브리핑이므로 비교 불가)" 한 줄로만 작성하라.\n\n' +
    "## 5. 분양·입지 전략 및 선제 대응\n" +
    "오늘 뉴스에서 도출되는 핵심 트렌드 2~3가지를 뽑고 아래 포맷으로만 작성하라.\n" +
    "① [전략 키워드]\n- 왜(근거): (내용)\n- 검토해 볼 만한 대응(제언): (내용 — 명령이 아닌 제언 어조)\n\n";

  if (rateBlock) {
    mainPrompt +=
      "## 6. 금융·심리 환경 정량 진단\n" +
      "오늘 의미 있게 움직였거나 뉴스와 직접 교차되는 지표만 골라 정량 진단하라. 변화 없는 지표는 생략.\n" +
      "가. 금리·시장금리 (방향·변동폭·PF금리 추정 — 변동이 있을 때)\n" +
      "나. 수분양자 자금조달 여건 (주담대·DSR 부담 정량화)\n" +
      "다. 소비자 심리 (주택가격전망·부채전망·CCSI 중 오늘 의미 있는 것)\n\n";
  } else {
    mainPrompt +=
      "## 6. 금융 환경 정성 진단\n" +
      "ECOS 데이터가 없으므로 뉴스 본문에서 추출 가능한 정성 신호만으로 진단하라. 2~3문장.\n\n";
  }

  if (hasFocus) {
    mainPrompt +=
      `## 7. 중점 분석 — ${focusPoint!.trim()}\n` +
      "위 분석과 별개로 이 이슈에 집중한 독자적 심층 분석을 작성하라. " +
      (rateBlock ? "관련 금리·CSI 수치가 있으면 반드시 인용. " : "") +
      "리스크와 기회 양면, 실행 가능한 대응 스탠스, 최소 6문장 이상.\n";
  }

  mainPrompt += "\n자, 최고의 브리핑 작성을 시작하라.";
  return mainPrompt;
}

const CONCLUSIONS_REGEX = /##\s*1\.\s*종합결론[^\n]*\r?\n([\s\S]*?)(?=\r?\n##\s|\s*$)/;

export function extractConclusions(report: string): string | null {
  const m = report.match(CONCLUSIONS_REGEX);
  if (!m) return null;
  const body = m[1].trim();
  return body || null;
}
